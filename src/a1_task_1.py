import tensorflow as tf
from tensorflow.keras import layers, models
import pandas as pd
from sklearn.model_selection import train_test_split
from sklearn.preprocessing import StandardScaler
import numpy as np
import matplotlib.pyplot as plt

# Load Fashion-MNIST Dataset
fashion_mnist = tf.keras.datasets.fashion_mnist
(x_train, y_train), (x_test, y_test) = fashion_mnist.load_data()

# Normalize the pixel values
x_train, x_test = x_train / 255.0, x_test / 255.0

# Flattening the input data to the MLP
x_train = x_train.reshape(-1, 28 * 28)
x_test = x_test.reshape(-1, 28 * 28)

# Defining the MLP Model
mlp_model = models.Sequential([
    layers.InputLayer(input_shape=(28 * 28,)),
    layers.Dense(128, activation='relu'),
    layers.Dense(64, activation='relu'),
    layers.Dense(10, activation='softmax')
])

# Compiling and training the model
optimizer = tf.keras.optimizers.Adam(learning_rate=0.0001)
mlp_model.compile(optimizer=optimizer, loss='sparse_categorical_crossentropy', metrics=['accuracy'])
mlp_model.fit(x_train, y_train, epochs=20, validation_data=(x_test, y_test), batch_size=32)

# Evaluate the model
test_loss, test_acc = mlp_model.evaluate(x_test, y_test)

# Print final test accuracy and test loss
print(f'Test accuracy: {test_acc}')
print(f'Test loss: {test_loss}')

# Experiment by reshaping input data for CNN
x_train_cnn = x_train.reshape(-1, 28, 28, 1)
x_test_cnn = x_test.reshape(-1, 28, 28, 1)

# Define the CNN model
cnn_model = models.Sequential([
    layers.Conv2D(32, (3, 3), activation='relu', input_shape=(28, 28, 1)),
    layers.MaxPooling2D((2, 2)),
    layers.Conv2D(64, (3, 3), activation='relu'),
    layers.MaxPooling2D((2, 2)),
    layers.Flatten(),
    layers.Dense(64, activation='relu'),
    layers.Dense(10, activation='softmax')

])

# Compile the model
cnn_model.compile(optimizer='adam', loss='sparse_categorical_crossentropy', metrics=['accuracy'])

# Training and evaluating the model
cnn_model.fit(x_train_cnn, y_train, epochs=20, validation_data=(x_test_cnn, y_test), batch_size=32)
test_loss, test_acc = cnn_model.evaluate(x_test_cnn, y_test)

# Print final test accuracy and loss
print(f'Test accuracy: {test_acc}')
print(f'Test loss: {test_loss}')

""" *CIFAR-10 Dataset*"""

# Load CIFAR-10 Dataset
cifar10 = tf.keras.datasets.cifar10
(x_train_cifar, y_train_cifar), (x_test_cifar, y_test_cifar) = cifar10.load_data()


# Normalizing the pixel values
x_train_cifar, x_test_cifar = x_train_cifar / 255.0, x_test_cifar / 255.0
y_train_cifar, y_test_cifar = y_train_cifar.flatten(), y_test_cifar.flatten()

# Define the CNN model for CIFAR-10
cnn_model = models.Sequential([
    layers.Conv2D(32, (3, 3), activation='relu', input_shape=(32, 32, 3)),
    layers.MaxPooling2D((2, 2)),
    layers.Conv2D(64, (3, 3), activation='relu'),
    layers.MaxPooling2D((2, 2)),
    layers.Conv2D(128, (3, 3), activation='relu'),
    layers.Flatten(),
    layers.Dense(128, activation='relu'),
    layers.Dense(10, activation='softmax')
])

# Compiling the model
optimizer = tf.keras.optimizers.Adam(learning_rate=0.0005)
cnn_model.compile(optimizer=optimizer, loss='sparse_categorical_crossentropy', metrics=['accuracy'])

# Training and evaluating the model
cnn_model.fit(x_train_cifar, y_train_cifar, epochs=20, validation_data=(x_test_cifar, y_test_cifar), validation_split=0.1, batch_size=32)
test_loss, test_acc = cnn_model.evaluate(x_test_cifar, y_test_cifar)

# Print the test accuracy and loss values
print(f'Test accuracy: {test_acc}')
print(f'Test loss: {test_loss}')